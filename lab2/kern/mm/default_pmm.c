/**
init_memmap的算法为：
1. 将所有可用的Page的flags设置为Property，引用计数设置为0,property设置为0,初始化page_link
2. 空闲块的第一个物理块的property设置为该空闲块的大小,将其加入到空闲链表末尾

只有操作系统可使用的物理页使用该函数进行初始化，
因此不能被使用的物理页的Page中的Reserved属性不会被修改，确保了所有的物理页都有正确的Page

初始化完成后的物理内存布局：
+-------------+-------------+--------------+------------+------------------------+
|             |   e820map   |              |Pages       |     Free memory        |
+-------------+-------------+--------------+------------+------------------------+
^             ^                            ^            ^                        ^
|             |                            |            |                        |
0          0x8000                 .bss段结束位置(end)  freemem                0x380000000

**/
static void
default_init_memmap(struct Page *base, size_t n) {
    // 遍历所有空闲物理页的Page结构，将Page结构的property置零（property只有在整个空闲块的第一个Page中才有效）
    assert(n > 0);
    struct Page *p = base;
    for (; p != base + n; p ++) {
        assert(PageReserved(p));
        p->flags = p->property = 0;  // 标记为空闲
        set_page_ref(p, 0); //清空物理页的引用计数
        SetPageProperty(p); // （函数在memlayout.h中声明）
    }
    base->property = n;
    SetPageProperty(base);
    nr_free += n;
    list_add(&free_list, &(base->page_link));
}

static struct Page *
default_alloc_pages(size_t n) {
    assert(n > 0);
    if (n > nr_free) { //没有足够的物理内存
        return NULL;
    }
    struct Page *page = NULL;
    list_entry_t *le = &free_list;
    while ((le = list_next(le)) != &free_list) { //寻找空闲块去分配
        struct Page *p = le2page(le, page_link);
        if (p->property >= n) {
            page = p;
            break;
        }
    }
    if (page != NULL) {// 如果找到了满足条件的空闲内存块
        for (struct Page *p = page; p != (page + n); ++p) {
            ClearPageProperty(p); // 将分配出去的内存页标记为非空闲
        }
        if (page->property > n) { //剩余空闲块链回空闲链表
            struct Page *p = page + n;
            p->property = page->property - n;  // 更新新的空闲块的大小信息
            list_add(&free_list, &(p->page_link));
        }
        list_del(&(page->page_link)); // 删除空闲链表中的原先的空闲块
        nr_free -= n; // 更新总空闲物理页的数量
    }
    return page;
}

//释放指定的某一物理页开始的若干个连续物理页
static void
default_free_pages(struct Page *base, size_t n) {
    assert(n > 0);
    struct Page *p = base;
    for (; p != base + n; p ++) {
        // 检查物理页是否真的被占用
        assert(!PageReserved(p) && !PageProperty(p));
        //设置flags将物理页标记为空闲
        p->flags = 0;
        //清空物理页的引用计数
        set_page_ref(p, 0);
    }
    // 设置空闲块大小
    base->property = n;
    SetPageProperty(base);
    list_entry_t *le = list_next(&free_list);
    while (le != &free_list) {
        // 获取page
        p = le2page(le, page_link);
        // 下一个空闲块
        le = list_next(le);
        // 判断这两空闲块是不是相邻的，不相邻无法进行合并
        if (base + base->property == p) {
            // p是base的后一个空闲块，合并
            base->property += p->property;
            // p不是空闲块第一个page，要使property无效
            ClearPageProperty(p);
            // 删除合并前的链接
            list_del(&(p->page_link));
        }
        else if (p + p->property == base) { // p是base的前一个空闲块
            p->property += base->property;
            ClearPageProperty(base);
            base = p;
            list_del(&(p->page_link));
        }
    }// 只是合并完，还未插入

    // 空闲页数量+n
    nr_free += n;
    le = list_next(&free_list);
    // 链表块的地址要由低往高连续，如果插入块后的末尾地址小于下一个块首地址，可以插入在这里（p之前）
    while (le != &free_list) {
        p = le2page(le, page_link);
        if (base + base->property <= p) {
            assert(base + base->property != p);
            break;
        }
        le = list_next(le);
    }
    list_add_before(le, &(base->page_link));
}

static size_t
default_nr_free_pages(void) {
    return nr_free;
}

static void
basic_check(void) {
    struct Page *p0, *p1, *p2;
    p0 = p1 = p2 = NULL;
    assert((p0 = alloc_page()) != NULL);
    assert((p1 = alloc_page()) != NULL);
    assert((p2 = alloc_page()) != NULL);

    assert(p0 != p1 && p0 != p2 && p1 != p2);
    assert(page_ref(p0) == 0 && page_ref(p1) == 0 && page_ref(p2) == 0);

    assert(page2pa(p0) < npage * PGSIZE);
    assert(page2pa(p1) < npage * PGSIZE);
    assert(page2pa(p2) < npage * PGSIZE);

    list_entry_t free_list_store = free_list;
    list_init(&free_list);
    assert(list_empty(&free_list));

    unsigned int nr_free_store = nr_free;
    nr_free = 0;

    assert(alloc_page() == NULL);

    free_page(p0);
    free_page(p1);
    free_page(p2);
    assert(nr_free == 3);

    assert((p0 = alloc_page()) != NULL);
    assert((p1 = alloc_page()) != NULL);
    assert((p2 = alloc_page()) != NULL);

    assert(alloc_page() == NULL);

    free_page(p0);
    assert(!list_empty(&free_list));

    struct Page *p;
    assert((p = alloc_page()) == p0);
    assert(alloc_page() == NULL);

    assert(nr_free == 0);
    free_list = free_list_store;
    nr_free = nr_free_store;

    free_page(p);
    free_page(p1);
    free_page(p2);
}

// LAB2: below code is used to check the first fit allocation algorithm (your EXERCISE 1) 
// NOTICE: You SHOULD NOT CHANGE basic_check, default_check functions!
static void
default_check(void) {
    int count = 0, total = 0;
    list_entry_t *le = &free_list;
    while ((le = list_next(le)) != &free_list) {
        struct Page *p = le2page(le, page_link);
        assert(PageProperty(p));
        count ++, total += p->property;
    }
    assert(total == nr_free_pages());

    basic_check();

    struct Page *p0 = alloc_pages(5), *p1, *p2;
    assert(p0 != NULL);
    assert(!PageProperty(p0));

    list_entry_t free_list_store = free_list;
    list_init(&free_list);
    assert(list_empty(&free_list));
    assert(alloc_page() == NULL);

    unsigned int nr_free_store = nr_free;
    nr_free = 0;

    free_pages(p0 + 2, 3);
    assert(alloc_pages(4) == NULL);
    assert(PageProperty(p0 + 2) && p0[2].property == 3);
    assert((p1 = alloc_pages(3)) != NULL);
    assert(alloc_page() == NULL);
    assert(p0 + 2 == p1);

    p2 = p0 + 1;
    free_page(p0);
    free_pages(p1, 3);
    assert(PageProperty(p0) && p0->property == 1);
    assert(PageProperty(p1) && p1->property == 3);

    assert((p0 = alloc_page()) == p2 - 1);
    free_page(p0);
    assert((p0 = alloc_pages(2)) == p2 + 1);

    free_pages(p0, 2);
    free_page(p2);

    assert((p0 = alloc_pages(5)) != NULL);
    assert(alloc_page() == NULL);

    assert(nr_free == 0);
    nr_free = nr_free_store;

    free_list = free_list_store;
    free_pages(p0, 5);

    le = &free_list;
    while ((le = list_next(le)) != &free_list) {
        assert(le->next->prev == le && le->prev->next == le);
        struct Page *p = le2page(le, page_link);
        count --, total -= p->property;
    }
    assert(count == 0);
    assert(total == 0);
}

